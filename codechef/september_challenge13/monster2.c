int monster(int x,int y) {
    int min = left[x][y];
    if(right[x][y+1]<min) min=right[x][y+1];
    if(top[x][y]<min) min=top[x][y];
    if(bottom[x+1][y]<min) min=bottom[x+1][y];
   //printf("%d\n",min);
    return prime[min];
}

